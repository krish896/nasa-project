const mongoose = require("mongoose");

const launchesSchema = new mongoose.Schema({
  flightNumber: {
    type: Number,
    required: true,
  },
  mission: {
    type: String,
    required: true,
  },
  rocket: {
    type: String,
    required: true,
  },
  launchDate: {
    type: Date,
    required: true,
  },
  target: {
    type: String,
    // required: true,
  },
  customers: [String],
  upcoming: {
    type: Boolean,
    required: true,
  },
  success: {
    type: Boolean,
    required: true,
  },
});

//first variable shoud be singular because mongoose will create a collection with plural name like here Launches because mongodb collections are in plural by default
module.exports = mongoose.model("Launch", launchesSchema);

// creates a Launch model and links it to the launches collection in MongoDB.
